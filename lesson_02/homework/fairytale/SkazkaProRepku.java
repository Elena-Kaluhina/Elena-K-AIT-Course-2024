package fairytale;

public class SkazkaProRepku {

    public static void main(String[] args) {
        System.out.println("Поучительная сказка про репку!");

        ded();
        repa();
        baba();
        vnuchka();
        zhuchka();
        kot();
        mushka();

        System.out.println("Вот и сказочке конец, а кто дочитал до конца, тот молодец!");

    }//end of main

    public static void ded(){

        System.out.println("Жили были дед и баба и была у них курочка Ряба.");
    }

    public static void repa(){

        System.out.println("Курочка Ряба была не простая, а золотая, потому что ела только репку.");

    }

    public static void baba(){

        System.out.println("И вот позвал дед бабку и сказал, чтобы она пошла в огород и вырвала для курочки репку.");
    }

    public static void vnuchka(){

        System.out.println("Бабка не смогла вытянуть сама и позвала внучку.");

    }

    public static void zhuchka(){

        System.out.println("Внучка позвала Жучку.");

    }

    public static void kot(){

        System.out.println("Жучка позвала кошку-сучку.");

    }

    public static void mushka(){

        System.out.println("Кошка позвала мышку-норушку. Вместе они и вытянули репку.");

    }



}//end of class
